class WheatstoneSolverAPI {
    constructor(worker) {
        this.worker = worker;
    }

    checkInput(v) {
        return (v.Uq * ((v.R1/(v.R1 + v.R2)) - (v.R3/(v.R3 + v.R4)))) === v.Um;
    }
}
